# 'Ord' returns an integer that represents the Unicode code point of that character.

print(ord('a'))
print(ord('A'))
print(ord('='))
print(ord('~'))

# 'Chr' is the invesre of 'ord'. It converts an integer to the corresponding Unicode character.

print(chr(97))
print(chr(65))
print(chr(61))
print(chr(126))
