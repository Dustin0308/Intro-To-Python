# What will the following code output? Explain why.

# count = 10
# count = count + 5
# print(count)

# Answer

# The code will output 15. The variable 'count' is initialized with a value of 10. It is then
# asked to take the original 'count' variable, which has a value of 10, and add 5 to it. Then
# it is asked to print the variable count, and 10 + 5 is 15, so it will output 15. 

count = 10
count = 10 + 5
print(count)
