# Which of these are valid variable names in Python? Which follow the recommended naming
# conventions. 

# a) my_var
# b) 2ndPlace
# c) first-name
# d) lastName
# e) CONSTANT_VALUE

# Answers

# a) This is idiomatic as it is lower snake_case and breaks no rules. 
# b) Illegal. Begins with a number. Also has capital letter. 
# c) Illegal. It has a hyphen which is the subtraction operator, even though it's all lower case.
# d) Non-idiomatic. Second word starts with capital letter and the words aren't separated by underscore.
# e) Non-idiomatic. Upper snake_case is reserved for constants, not variables. 
