# What is the difference between a variable and a constant in Python? How are they distinguished by
# naming conventions?

# Answer: Values are assigned to variables and constants, but variables can be changed or 
# modified, while constant cannot. Python doesn't have a true knowing of constant objects,
# but programmers who follow good practice make sure constants do not change. Constants are 
# typically written at the top of the program and remain constant throughout that program. 
# Naming conventions for variables should include all lower case letter (a-z), digits (0-9),
# and should use use underscores for word separation. They should not start with a digit. 
# Constants are all uppercase letters (A_Z), can include numbers (0-9), should be separated
# by underscores when several words are used, and should also not start with a digit. 
