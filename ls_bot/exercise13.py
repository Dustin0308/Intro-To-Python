# Which of these variable names follow proper Python conventions? Explain why or why not for each.

# a) user_age               
# b) UserAge
# c) 1st_place
# d) student_name
# e) student-name

# Answers

# a) Proper. Lower case with underscores and doesn't start with a digit or break any rules.
# b) Improper. Is in PascalCase, which is reserved for class naming. 
# c) Improper. Starts with an number. 
# d) Proper. Lower case with underscores and doesn't start with a digit or break any rules. 
# e) Improper. While it is lowercase, the words are separated by a hyphen and not an underscore.
